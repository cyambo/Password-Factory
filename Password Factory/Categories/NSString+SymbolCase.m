#import "NSString+SymbolCase.h"
#import "NSString+MapCase.h"
static NSDictionary *map = nil;
                             
                             
@implementation NSString (SymbolCase)
-(NSString *)symbolCase:(float)percent {

    map = @{
    @"A" : @[ @"4",  @"/-\\",  @"@",  @"^",  @"/\\"],
    @"B" : @[ @"8",  @"]3",  @"]8",  @"|3",  @"|8",  @"13"],
    @"C" : @[ @"(",  @"{",  @"[[",  @"<"],
    @"D" : @[ @")",  @"[}",  @")",  @"}"],
    @"E" : @[ @"3"],
    @"F" : @[ @"|=", @"(=", @"]="],
    @"G" : @[ @"6",  @"9",  @"(_>",  @"[[6",  @"&",  @"("],
    @"H" : @[ @"#",  @"|-|",  @"(-)",  @")-(",  @"}{",  @"}-{",  @"|~|"],
    @"I" : @[ @"1",  @"!",  @"|"],
    @"J" : @[ @"_|"],
    @"K" : @[ @"|<",  @"|{",  @"]<",  @"]<"],
    @"L" : @[ @"|", @"1", @"|_"],
    @"M" : @[ @"/\\/\\",  @"|\\/|",  @"/V\\",  @"^^"],
    @"N" : @[ @"/\\/",  @"|\\|",  @"~"],
    @"O" : @[ @"0",  @"()",  @"[]",  @"<>",  @"*"],
    @"P" : @[ @"|D",  @"|*",  @"|>"],
    @"Q" : @[ @"()",  @"0"],
    @"R" : @[ @"2"],
    @"S" : @[ @"5", @"$"],
    @"T" : @[ @"7",  @"+"],
    @"U" : @[ @"|_|",  @"\\_\\",  @"/_/"],
    @"V" : @[ @"\\/" ],
    @"W" : @[ @"\\/\\/",  @"|/\\|",  @"[/\\]",  @"(/\\)",  @"VV"],
    @"X" : @[ @"><",  @"}{",  @")("],
    @"Y" : @[ @"'/",  @"%",  @"`/"],
    @"Z" : @[ @"2",  @"7_"]
    };
    
    return [[self uppercaseString] mapCase:percent map:map];
}


@end
